    /***
     * 파일 확장자 및 mime type을 추가 한다.
     * @param ext 파일 확장자 ex) gif, bmp, png
     * @param mimeType ex) application/octet-stream,application/vnd.ms-excel
     * @param force 강제로 추가 할것인지 유무. 파일 확장자가 이미 존재 할 경우 덮어 씌운다.
     * @return boolean
     */
    public static boolean addAllowExtAndMimeType(String ext,String mimeType, boolean force) {
    	
    	
    	boolean isContainsKey = WELL_KNOWN_MIME_TYPES.containsKey(ext);
    	
    	if(isContainsKey) {

    		if(force) {
                WELL_KNOWN_MIME_TYPES.put(ext, mimeType);    		
    		}
    		
    		log.warn("The extension exists. Change to (force = true) value to overwrite.");

    	}else {
            WELL_KNOWN_MIME_TYPES.put(ext, mimeType);    		
    	}
    	
        return WELL_KNOWN_MIME_TYPES.containsKey(ext) && WELL_KNOWN_MIME_TYPES.containsValue(mimeType);
    }
